    private static void fillEmployeeDropDown() throws SQLException, Exception {
        
        try {
            ResultSet result = dbConnect.getConnection().createStatement().
                    executeQuery("SELECT * FROM tblemployee");
            dicEmployees = new HashMap<>();

            while (result.next()) {
                dicEmployees.put(result.getString("EmployeeID"), result.getString("empName"));
            }

            dicEmployees.values().forEach((clientName) -> {
                ddlEmployee.addItem(clientName);
            });

        } catch (SQLException ex) {
            throw ex;
        }
    }
