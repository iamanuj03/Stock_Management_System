    private void btnBonusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBonusActionPerformed
        // TODO add your handling code here:
        String empID="";
        int salary=0;
        String status="";
        double bonus=0.0;
        try {
         for(Entry<String,String> entry:dicEmployees.entrySet())
        {
            if(entry.getValue().equals(ddlEmployee.getSelectedItem())){
                empID= entry.getKey();
            }
        }
        PreparedStatement psGetSalary = dbConnect.getConnection().
        prepareStatement("SELECT * FROM tblemployee WHERE EmployeeID=?");
        psGetSalary.setString(1, empID);
        ResultSet result = psGetSalary.executeQuery();
        while (result.next()) {
            txtSalary.setText(result.getString("salary"));
            salary=Integer.parseInt(result.getString("salary"));
            status=result.getString("status");
        }
        
        if(status.equals("Permanent"))
        {
            permanentEmployee pe=new permanentEmployee(0,"");
            bonus=pe.CalculateBonus(salary);
        }
        if(status.equals("Temporary"))
        {
            temporaryEmployee te=new temporaryEmployee(0,"");
            bonus=te.CalculateBonus(salary);
        }
        
        txtBonus.setText(Double.toString(bonus));
        }
        catch (Exception ex) {
            Logger.getLogger(frmOrderGenerator.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_btnBonusActionPerformed

    
